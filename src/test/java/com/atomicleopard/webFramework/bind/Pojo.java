package com.atomicleopard.webFramework.bind;
public class Pojo {
	private String name;
	private String value;

	public Pojo(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
}
